#Get present working directory.
pwd

#Move into a child directory or path.
cd DIR_NAME/PATH
ca 'DIR WITH SPACES'

#Find all directories starts with.
cd ~/Doc #Now, enter TAB twice...

#Go back to the parent directory.
cd ..

#List the items in the current folder.
ls

#List all the items in the current folder including hidden files.
ls -a

#Get a detailed view of the items in the current folder.
ls -l

#Command options could be concatenated!
#e.g. Get a detailed view of the items ordered by last update time.
ls -lt

#Create a folder.
mkdir FOLDER_NAME

